import math

print("round(2.3)", round(2.3)) #2
print("round(2.7)", round(2.7)) #3
print("round(2.5)", round(2.5)) #2

print("floor", math.floor(4.7)) #4
print("ceil", math.ceil(4.5)) #5

print("pi", math.pi) #3.141592653589793
print("e", math.e) #2.718281828459045

print("sin", math.sin(math.pi/2)) #1.0
print("cos", math.cos(0)) #1.0

print("hypot", math.hypot(3,4)) #5.0

print("pow", math.pow(4, 3)) #64.0
print("**", 4**3) #64

print("log", math.log(12)) #2.4849066497880004
print("log10", math.log10(1000)) #3.0
